export const getProductosPorCategoria = (categoria) => {
    return new Promise((res, rej) => {
        setTimeout(()=> {
            res(productos.filter(prod => prod.category === categoria))
        }, 500)
    })
}

export const getProducto = (id) => {
    return new Promise ((res,rej) => {
        setTimeout(()=> {
            res(productos.find(prod => prod.id === id*1))
        }, 500)
    })
}